#include "ft_printf.h"

int	ft_pnbr(int n)
{
	long	ln;
	int		i;

	i = 0;
	ln = n;
	if (ln < 0)
	{
		i += ft_pchar('-');
		ln *= -1;
	}
	if (ln <= 9)
		i += ft_pchar(ln + '0');
	else
	{
		i += ft_pnbr(ln / 10);
		i += ft_pnbr(ln % 10);
	}
	return (i);
}
