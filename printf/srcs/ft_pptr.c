#include "ft_printf.h"

unsigned long	ft_pptr(unsigned long n)
{
	int	i;

	i = 0;
	if (n == 0)
		i += ft_pstr ("(nil)");
	else
	{
		i += ft_pstr("0x");
		i += ft_phexa(n, 'x');
	}
	return (i);
}
